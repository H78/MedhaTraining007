CREATE TABLE `profile` (
  `name` char(52) CHARACTER SET latin1 DEFAULT NULL,
  `lastname` char(52) CHARACTER SET latin1 DEFAULT NULL,
  `emailID` char(52) DEFAULT NULL,
  `DOB` varchar(20) DEFAULT NULL,
  `address` char(100) CHARACTER SET latin1 DEFAULT NULL,
  `contactNo` bigint(11) DEFAULT NULL,
  `emergencyContactName` char(52) CHARACTER SET latin1 DEFAULT NULL,
  `emergencyContactNo` bigint(11) DEFAULT NULL,
  `accountNum` int(11) NOT NULL AUTO_INCREMENT,
  PRIMARY KEY (`accountNum`)
) ENGINE=InnoDB AUTO_INCREMENT=15 DEFAULT CHARSET=utf8;
